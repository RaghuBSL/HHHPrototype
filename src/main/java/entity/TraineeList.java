package entity;

/**
 *
 * @author S Ragothaman
 */
public class TraineeList {
    
    private Integer  traineeID; 
    private String  traineeName;
    private String  gender;                            
    private String  dateOfBirth;
    private String  caste;
    private String  religion;
    private String  maritalStatus;
    private String  annualIncome;
    private String  landholding;
    private String  status;

    /**
     * @return the traineeID
     */
    public Integer getTraineeID() {
        return traineeID;
    }

    /**
     * @param traineeID the traineeID to set
     */
    public void setTraineeID(Integer traineeID) {
        this.traineeID = traineeID;
    }

    /**
     * @return the traineeName
     */
    public String getTraineeName() {
        return traineeName;
    }

    /**
     * @param traineeName the traineeName to set
     */
    public void setTraineeName(String traineeName) {
        this.traineeName = traineeName;
    }

    /**
     * @return the gender
     */
    public String getGender() {
        return gender;
    }

    /**
     * @param gender the gender to set
     */
    public void setGender(String gender) {
        this.gender = gender;
    }

    /**
     * @return the dateOfBirth
     */
    public String getDateOfBirth() {
        return dateOfBirth;
    }

    /**
     * @param dateOfBirth the dateOfBirth to set
     */
    public void setDateOfBirth(String dateOfBirth) {
        this.dateOfBirth = dateOfBirth;
    }

    /**
     * @return the caste
     */
    public String getCaste() {
        return caste;
    }

    /**
     * @param caste the caste to set
     */
    public void setCaste(String caste) {
        this.caste = caste;
    }

    /**
     * @return the religion
     */
    public String getReligion() {
        return religion;
    }

    /**
     * @param religion the religion to set
     */
    public void setReligion(String religion) {
        this.religion = religion;
    }

    /**
     * @return the maritalStatus
     */
    public String getMaritalStatus() {
        return maritalStatus;
    }

    /**
     * @param maritalStatus the maritalStatus to set
     */
    public void setMaritalStatus(String maritalStatus) {
        this.maritalStatus = maritalStatus;
    }

    /**
     * @return the annualIncome
     */
    public String getAnnualIncome() {
        return annualIncome;
    }

    /**
     * @param annualIncome the annualIncome to set
     */
    public void setAnnualIncome(String annualIncome) {
        this.annualIncome = annualIncome;
    }

    /**
     * @return the landholding
     */
    public String getLandholding() {
        return landholding;
    }

    /**
     * @param landholding the landholding to set
     */
    public void setLandholding(String landholding) {
        this.landholding = landholding;
    }

    /**
     * @return the status
     */
    public String getStatus() {
        return status;
    }

    /**
     * @param status the status to set
     */
    public void setStatus(String status) {
        this.status = status;
    }

}
